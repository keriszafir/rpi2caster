/*
   askcol:  asks the column-name

   NI= 0,  NL=1 other combinations Nx = 15
   A-O = 2-16

   version 27-12-2003

 */
unsigned int askcol()
{
    unsigned int i;
    char c, c1;

    do {
       i = - 2;
       do {
	 print_at(23,20,"                    ");
	 print_at(23,20,"column = ");
	 get_line();
	 c = r_buff[0];
       } while ( c != 'A' && c != 'B' && c != 'C' && c != 'D' && c != 'E'
	      && c != 'F' && c != 'G' && c != 'H' && c != 'I' && c != 'J'
	      && c != 'K' && c != 'L' && c != 'M' && c != 'N' && c != 'O' );
       switch (c) {
	  case 'N' :
	    c1 = r_buff[1];
	    switch (c1) {
	       case 'I' : i =  0; break;
	       case 'L' : i =  1; break;
	       default  : i = 15; break ;
	    }
	    break;
	  default :
	    i = c - 'A' + 2 ;
	    break;
       }
    }
      while ( !( (i >= 0 ) && (i <= 16) ) );

    return (i);
}

